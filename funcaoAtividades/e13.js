function mostraDia(dia) {
    switch (dia) {
        case 1:
            console.log('Domingo')
            break
        case 2:
            console.log('Segunda')
            break
        case 3:
            console.log('Terça')
            break
        case 4:
            console.log('Quarta')
            break
        case 5:
            console.log('Quinta')
            break
        case 6:
            console.log('Sexta')
            break
        case 7:
            console.log('Sábado')
            break
        default:
            console.log('Dia invalido')
    }
}

mostraDia(1)
mostraDia(2)
mostraDia(3)
mostraDia(4)
mostraDia(5)
mostraDia(6)
mostraDia(7)
mostraDia(8)